package matrix;

public class Pixel {
    private float R;
    private float G;
    private float B;
    public Pixel(float R,float G,float B){
        this.R=R;
        this.G=G;
        this.B=B;
    }
    public float getR(){
        return this.R;
    }
    public float getG(){
        return this.G;
    }
    public float getB(){
        return this.B;
    }

}
